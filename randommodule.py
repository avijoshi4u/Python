from random import *

# Generate random value between 0 and 1 excluding 0, 1
for i in range(5):
    print(random())

# Generate random value between 20 and 30
for i in range(10):
    print(randint(20,30))

# Generate random floating value between 1 and 50
for i in range(10):
    print(uniform(1,50))

# Generate random value between 60 and 70 and the difference between them will be three
for i in range(10):
    print(randrange(60,70,3))

