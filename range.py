# print value from 0 to 4
r = range(5)
for i in r:
    print(i)

# if you want to start from particular value
# in the below excludes the last value
r1 = range(1, 6)
print("2nd for loop")
for k in r1:
    print(k)

# if you want to include steps
r2 = range(1, 15, 3)
print("3rd for loop")
for l in r2:
    print(l)
